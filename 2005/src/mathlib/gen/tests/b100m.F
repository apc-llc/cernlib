     +  ''KB'',6X,''DB'',8X,''B'',23X,''DBH'',13X,''BH'')')
#endif
#if !defined(CERNLIB_DOUBLE)
     +  ''KB'',6X,''B'',13X,''BH'')')
#endif

      J= 1
      DO 1 N = -8,8,2
      DO 1 K = -1,4
      KB=KBINOM(N,K)
      IF (KEX1(J) .NE. 0) THEN
      KER1=ABS((KB-KEX1(J))/KEX1(J))
      ELSE
      KER1=ABS(KB-KEX1(J))
      ENDIF
      KERMAX=MAX(KERMAX,KER1)
      DX=N
#if defined(CERNLIB_DOUBLE)
      D1=DBINOM(DX,K)
      IF (EX2(J) .NE. 0) THEN
      ER2=ABS((D1-EX2(J))/EX2(J))
      ELSE
      ER2=ABS(D1-EX2(J))
      ENDIF
      ERMAX=MAX(ERMAX,ER2)
#endif
      RDX=DX
      RD1=BINOM(RDX,K)
      IF (REX3(J) .NE. 0) THEN
      RER3=ABS((RD1-REX3(J))/REX3(J))
      ELSE
      RER3=ABS(RD1-REX3(J))
      ENDIF
      RERMAX=MAX(RERMAX,RER3)
      DX=N+HALF
#if defined(CERNLIB_DOUBLE)
      D2=DBINOM(DX,K)
      IF (EX2(J) .NE. 0) THEN
      ER4=ABS((D2-EX4(J))/EX4(J))
      ELSE
      ER4=ABS(D2-EX4(J))
      ENDIF
      ERMAX=MAX(ERMAX,ER4)
#endif
      RDX=DX
      RD2=BINOM(RDX,K)
      IF (REX5(J) .NE. 0) THEN
      RER3=ABS((RD2-REX5(J))/REX5(J))
      ELSE
      RER5=ABS(RD2-REX5(J))
      ENDIF
      RERMAX=MAX(RERMAX,RER5)
      J=J+1
#if !defined(CERNLIB_DOUBLE)
    1 WRITE(LOUT,'(5X,I4,D10.1,E16.1)') KER1,RER3,RER5
#endif
#if defined(CERNLIB_DOUBLE)
    1 WRITE(LOUT,'(5X,I4,D10.1,E10.1,D25.1,E16.1)')
     +              KER1,ER2,RER3,ER4,RER5
#endif
