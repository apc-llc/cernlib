      ERRMAX=MAX(ERRMAX,ABS(R-T))
      WRITE(LOUT,100) X(1),R,T,R-T
#if defined(CERNLIB_DOUBLE)
      R=DCHSUM(2,C(0,2),N(2),X(2))
#endif
