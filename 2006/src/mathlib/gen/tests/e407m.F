      ERRMAX=MAX(ERRMAX,ABS(R-T))
      WRITE(LOUT,100) X(2),R,T,R-T
#if defined(CERNLIB_DOUBLE)
      R=DCHSUM(3,C(0,3),N(3),X(3))
#endif
#if !defined(CERNLIB_DOUBLE)
      R= CHSUM(3,C(0,3),N(3),X(3))
#endif
      T=ATAN(X(3))
      ERRMAX=MAX(ERRMAX,ABS(R-T))
      WRITE(LOUT,100) X(3),R,T,R-T
