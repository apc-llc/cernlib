      R=DCHSUM(4,C(0,4),N(4),X(4))
#endif
#if !defined(CERNLIB_DOUBLE)
      R= CHSUM(4,C(0,4),N(4),X(4))
#endif
      T=1/(1+X(4))
      ERRMAX=MAX(ERRMAX,ABS(R-T))
      WRITE(LOUT,100) X(4),R,T,R-T
  100 FORMAT(1X,F10.1,2F35.25,1P,D10.1/)
